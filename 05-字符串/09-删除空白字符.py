mystr = "   hello world and cess and success and Python   "

# lstrip() 删除开头空白
print(mystr.lstrip()) # ‘hello world and cess and success and Python   ’

# rstrip() 删除结尾空白
print(mystr.rstrip()) # ‘   hello world and cess and success and Python’

# strip() 删除两侧空白
print(mystr.strip()) # ‘hello world and cess and success and Python’