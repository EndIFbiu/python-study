mystr = "hello world and cess and success and Python"

# 1.capitalize() 首字母大写，其他都改小写，注意python
print(mystr.capitalize())  # Hello world and cess And success and python

# 2.title() 每个单词首字母均大写
print(mystr.title())  # Hello World And Cess And Success And Python

# 3.lower() 全部小写
print(mystr.lower())  # hello world and cess and success and python

# 4.upper() 全部大写
print(mystr.upper())  # HELLO WORLD AND CESS AND SUCCESS AND PYTHON
